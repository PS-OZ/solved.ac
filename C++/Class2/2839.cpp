/*
 * 문제: 백준 2839번 - 설탕 배달
 * 링크: https://www.acmicpc.net/problem/2839
 * 분류: 그리디, 구현
 *
 * 문제 설명:
 * - 상근이는 N킬로그램의 설탕을 3kg, 5kg 봉지로 정확히 나눠 배달해야 한다.
 * - 봉지 개수를 최소로 하여 설탕을 나누는 방법을 구하고, 불가능하면 -1을 출력한다.
 *
 * 입력:
 * - 첫째 줄에 N (3 ≤ N ≤ 5000)이 주어진다.
 *
 * 출력:
 * - 봉지의 최소 개수 또는 정확히 나눌 수 없으면 -1을 출력한다.
 *
 * 접근 방법:
 * - 5kg 봉지를 최대한 많이 사용하고, 남은 무게를 3kg 봉지로 채운다.
 * - 5로 나누어떨어지지 않으면 N에서 3씩 빼면서 5로 나누어떨어지는지 확인한다.
 * - N이 0이 되면 봉지 개수 출력, N이 0보다 작아지면 -1 출력.
 *
 * 시간복잡도: O(N)
 * 공간복잡도: O(1)
 */

#include <iostream>
using namespace std;

int main() {
    int N, count = 0;
    cin >> N;

    while (true) {
        if (N % 5 == 0) {          // 5로 나누어떨어지면
            count += N / 5;
            cout << count << '\n';
            break;
        }
        N -= 3;                    // 3kg 봉지 하나 사용
        count++;
        if (N < 0) {               // 정확히 나눌 수 없는 경우
            cout << -1 << '\n';
            break;
        }
    }
    return 0;
}
