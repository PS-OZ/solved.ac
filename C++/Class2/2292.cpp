/*
 * 문제: 백준 2292번 - 벌집
 * 링크: https://www.acmicpc.net/problem/2292
 * 분류: 구현, 수학
 * 
 * 문제 설명:
 * - 육각형으로 이루어진 벌집에서 중앙 1번 방부터 시작해 N번 방까지 최소 몇 개의 방(시작과 끝 포함)을 지나가야 하는지 구하는 문제.
 * 
 * 입력:
 * - 첫째 줄에 N(1 ≤ N ≤ 1,000,000,000)이 주어진다.
 * 
 * 출력:
 * - 중앙 1번 방에서 N번 방까지 최소 개수의 방을 지나서 갈 때 몇 개의 방을 지나는지 출력한다.
 * 
 * 접근 방법:
 * - 벌집은 중앙(1번 방)에서부터 바깥으로 갈수록 6의 배수만큼 방이 늘어난다.
 * - 각 층(겹)마다 방의 개수는 1, 6, 12, 18, ... 순으로 증가한다.
 * - N이 속한 층(겹)의 번호가 곧 최소 이동 거리(지나는 방의 개수)가 된다.
 *   - 1층: 1
 *   - 2층: 2~7
 *   - 3층: 8~19
 *   - 4층: 20~37
 *   - ...
 * - while문으로 누적 방 개수를 계산하며 N이 그 값 이하가 되는 순간의 층 번호를 출력한다.
 * 
 * 시간복잡도: O(√N) 이하 (N이 매우 커도 반복 횟수는 적음)
 * 공간복잡도: O(1)
 */

#include <iostream>
using namespace std;

int main() {
    int N;
    cin >> N;

    int cnt = 1;      // 겹(층) 수, 시작은 1
    int range = 1;    // 현재 겹의 마지막 방 번호

    // N이 현재 겹의 마지막 방 번호를 넘을 때까지 반복
    while (N > range) {
        range += 6 * cnt; // 다음 겹의 마지막 방 번호로 이동
        cnt++;            // 겹(층) 수 증가
    }

    cout << cnt;
    return 0;
}
