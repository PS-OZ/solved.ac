/*
 * 문제: 백준 10172번 - 개
 * 링크: https://www.acmicpc.net/problem/10172
 * 분류: 구현, 출력
 *  
 * 문제 설명:
 * - 아래 예제와 같이 개를 출력하는 문제.
 * - 역슬래시(\), 큰따옴표(") 등 특수문자 출력에 주의해야 한다.
 * 
 * 입력:
 * - 없음
 * 
 * 출력:
 * - 아래와 같은 개 그림을 정확히 출력한다.
 *   |\_/|
 *   |q p|   /}
 *   ( 0 )"""\
 *   |"^"`    |
 *   ||_/=\\__|
 * 
 * 접근 방법:
 * - 역슬래시(\)는 \\로, 큰따옴표(")는 \"로 이스케이프하여 출력한다.
 * - 각 줄마다 정확한 공백과 특수문자 위치에 유의한다.
 * 
 * 시간복잡도: O(1)
 * 공간복잡도: O(1)
 */

#include <iostream>
using namespace std;

int main() {
    cout << "|\\_/|\n";
    cout << "|q p|   /}\n";
    cout << "( 0 )\"\"\"\\\n";
    cout << "|\"^\"`    |\n";
    cout << "||_/=\\\\__|";
    return 0;
}
