/*
 * 문제: 백준 11720번 - 숫자의 합
 * 링크: https://www.acmicpc.net/problem/11720
 * 분류: 구현, 문자열, 수학
 * 
 * 문제 설명:
 * - N개의 숫자가 공백 없이 하나의 문자열로 주어진다.
 * - 이 숫자들의 합을 구해 출력한다.
 * 
 * 입력:
 * - 첫째 줄에 숫자의 개수 N(1 ≤ N ≤ 100)이 주어진다.
 * - 둘째 줄에 N개의 숫자가 공백 없이 문자열로 주어진다.
 * 
 * 출력:
 * - 입력으로 주어진 숫자 N개의 합을 출력한다.
 * 
 * 접근 방법:
 * - 숫자 문자열을 한 글자씩 읽으면서, 각 문자를 정수로 변환해 합산한다.
 * 
 * 시간복잡도: O(N)
 * 공간복잡도: O(1)
 */

#include <iostream>
using namespace std;

int main() {
    int N, sum = 0;
    cin >> N;           // 숫자의 개수 입력
    string s;
    cin >> s;           // 숫자 문자열 입력

    for (int i = 0; i < N; i++) {
        sum += s[i] - '0'; // 각 문자를 정수로 변환해 합산
    }

    cout << sum;
    return 0;
}
