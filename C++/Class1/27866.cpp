/*
 * 문제: 백준 27866번 - 문자와 문자열
 * 링크: https://www.acmicpc.net/problem/27866
 * 분류: 문자열, 구현
 * 
 * 문제 설명:
 * - 문자열 S와 정수 i가 주어진다.
 * - S의 i번째 문자를 출력한다. (단, S의 인덱스는 1부터 시작)
 * 
 * 입력:
 * - 첫째 줄에 문자열 S(1 ≤ S의 길이 ≤ 1,000)가 주어진다.
 * - 둘째 줄에 정수 i가 주어진다. (1 ≤ i ≤ S의 길이)
 * 
 * 출력:
 * - S의 i번째 문자를 출력한다.
 * 
 * 접근 방법:
 * - 문자열 S를 입력받고, i를 입력받아 S[i-1]번째 문자를 출력한다.
 *   (C++ 문자열 인덱스는 0부터 시작하므로, i-1로 접근)
 * 
 * 시간복잡도: O(1)
 * 공간복잡도: O(1)
 */

#include <iostream>
#include <string>
using namespace std;

int main() {
    string S;
    int i;
    cin >> S >> i;         // 문자열과 정수 입력
    cout << S[i - 1];      // 1-based 인덱스이므로 i-1번째 문자 출력
    return 0;
}
