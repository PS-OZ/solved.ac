/*
 * 문제: 백준 10950번 - A+B - 3
 * 링크: https://www.acmicpc.net/problem/10950
 * 분류: 구현, 반복문
 *  
 * 문제 설명:
 * - 첫째 줄에 테스트 케이스의 개수 T가 주어진다.
 * - 각 테스트 케이스마다 두 정수 A와 B(0 < A, B < 10)가 주어진다.
 * - 각 테스트 케이스마다 A+B를 한 줄에 하나씩 출력한다.
 * 
 * 입력:
 * - 첫째 줄: 테스트 케이스 개수 T
 * - 둘째 줄부터 T개의 줄에 각 줄마다 A와 B가 공백으로 구분되어 주어진다.
 * 
 * 출력:
 * - 각 테스트 케이스마다 A+B를 한 줄에 하나씩 출력한다.
 * 
 * 접근 방법:
 * - T를 입력받고, for문을 T번 반복하며 각 줄에서 두 수를 입력받아 합을 출력한다.
 * 
 * 시간복잡도: O(T)
 * 공간복잡도: O(1)
 */

#include <iostream>
using namespace std;

int main() {
    int T, A, B;
    cin >> T; // 테스트 케이스 개수 입력

    for (int i = 0; i < T; i++) {
        cin >> A >> B;           // 두 정수 입력
        cout << A + B << '\n';   // 합 출력
    }

    return 0;
}
