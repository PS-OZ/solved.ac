/*
 * 문제: 백준 10952번 - A+B - 5
 * 링크: https://www.acmicpc.net/problem/10952
 * 분류: 구현, 반복문, 입출력
 *  
 * 문제 설명:
 * - 두 정수 A와 B를 입력받아 A+B를 출력하는 프로그램을 작성한다.
 * - 입력은 여러 개의 테스트 케이스로 이루어져 있으며, 마지막 입력은 "0 0"이다.
 * - "0 0"이 입력되면 프로그램을 종료한다.
 * 
 * 입력:
 * - 각 테스트 케이스는 한 줄로 이루어져 있으며, 각 줄에 A와 B가 주어진다. (0 < A, B < 10)
 * - 입력의 마지막에는 0 두 개가 들어온다.
 * 
 * 출력:
 * - 각 테스트 케이스마다 A+B를 출력한다.
 * 
 * 접근 방법:
 * - 무한 반복문(while(1) 또는 while(true))을 사용하여 입력을 계속 받는다.
 * - 입력받은 두 수가 모두 0이면 break로 반복문을 종료한다.
 * - 그렇지 않으면 두 수의 합을 출력한다.
 * 
 * 시간복잡도: O(T) (T: 테스트 케이스 개수)
 * 공간복잡도: O(1)
 */

#include <iostream>
using namespace std;

int main() {
    int a, b;
    while (true) {
        cin >> a >> b;                 // 두 정수 입력
        if (a == 0 && b == 0) break;   // 종료 조건
        cout << a + b << '\n';         // 합 출력
    }
    return 0;
}
