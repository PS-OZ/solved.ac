/*
 * 문제: 백준 2739번 - 구구단
 * 링크: https://www.acmicpc.net/problem/2739
 * 분류: 구현, 반복문
 *  
 * 문제 설명:
 * - 정수 N(1 ≤ N ≤ 9)이 주어진다.
 * - N단 구구단(1부터 9까지 곱한 결과)을 출력한다.
 * - 출력 형식은 "N * i = X" (i는 1~9, X는 N*i)로 각 줄마다 한 번씩 출력한다.
 * 
 * 입력:
 * - 첫째 줄에 N이 주어진다.
 * 
 * 출력:
 * - N * 1 = N
 * - N * 2 = 2N
 * - ...
 * - N * 9 = 9N
 * (각 줄마다 하나씩 출력)
 * 
 * 접근 방법:
 * - N을 입력받고, 1부터 9까지 반복문을 돌며 각 곱셈 결과를 출력한다.
 * 
 * 시간복잡도: O(1) (반복 횟수 고정)
 * 공간복잡도: O(1)
 */

#include <iostream>
using namespace std;

int main() {
    int n;
    cin >> n; // 구구단 단수 입력 받기

    for (int i = 1; i <= 9; i++) { // 1부터 9까지 곱셈
        cout << n << " * " << i << " = " << n * i << '\n';
    }

    return 0;
}
