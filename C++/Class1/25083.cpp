/*
 * 문제: 백준 25083번 - 새싹
 * 링크: https://www.acmicpc.net/problem/25083
 * 분류: 구현, 출력
 *  
 * 문제 설명:
 * - 예제와 같이 새싹 모양을 정확히 출력하는 문제.
 * - 역슬래시(\), 큰따옴표(") 등 특수문자와 공백, 줄바꿈을 정확히 맞춰야 한다.
 * 
 * 입력:
 * - 없음
 * 
 * 출력:
 * - 아래와 같은 새싹 그림을 정확히 출력한다.
 *   ,r'"7
 *   r`-_ ,' ,/
 *    \. ". L_r'
 *    `~\/
 *      |
 *      |
 * 
 * 접근 방법:
 * - 역슬래시(\)는 \\로, 큰따옴표(")는 \"로 이스케이프하여 출력한다.
 * - 각 줄의 공백, 특수문자, 줄바꿈(\n)을 예제와 완전히 동일하게 맞춘다.
 * 
 * 시간복잡도: O(1)
 * 공간복잡도: O(1)
 */

#include <iostream>
using namespace std;

int main() {
    cout << " ,r'\"7" << '\n';
    cout << "r`-_ ,' ,/" << '\n';
    cout << " \\. \". L_r'" << '\n';
    cout << " `~\\/" << '\n';
    cout << "   |" << '\n';
    cout << "   |";
    return 0;
}
