/*
 * 문제: 백준 2577번 - 숫자의 개수
 * 링크: https://www.acmicpc.net/problem/2577
 * 분류: 구현, 수학, 배열
 * 
 * 문제 설명:
 * - 세 개의 자연수 A, B, C가 주어진다.
 * - A × B × C를 계산한 결과에 0부터 9까지 각 숫자가 몇 번씩 쓰였는지 출력한다.
 * 
 * 입력:
 * - 첫째 줄에 A, 둘째 줄에 B, 셋째 줄에 C가 주어진다. (각각 100 ≤ A, B, C < 1000)
 * 
 * 출력:
 * - 첫째 줄부터 열 번째 줄까지, 0부터 9까지 각 숫자가 몇 번씩 쓰였는지를 한 줄에 하나씩 출력한다.
 * 
 * 접근 방법:
 * - A, B, C를 곱한 결과를 문자열로 변환하거나, 각 자리수를 나눗셈과 나머지 연산으로 추출한다.
 * - 0~9까지의 개수를 저장할 배열을 선언하여 각 숫자가 등장할 때마다 카운트한다.
 * 
 * 시간복잡도: O(N) (N = 결과값의 자릿수, 최대 9자리)
 * 공간복잡도: O(1)
 */

#include <iostream>
using namespace std;

int main() {
    int a, b, c;
    cin >> a >> b >> c;

    int result = a * b * c;
    int count[10] = {0}; // 0~9까지 각 숫자의 개수 저장

    // 결과값의 각 자리수를 이용해 카운트
    do {
        count[result % 10]++; // 마지막 자리 숫자 카운트
        result /= 10;         // 마지막 자리 제거
    } while (result > 0);

    // 0부터 9까지 각 숫자의 등장 횟수 출력
    for (int i = 0; i < 10; i++) {
        cout << count[i] << "\n";
    }

    return 0;
}
