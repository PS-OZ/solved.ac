/*
 * 문제: 백준 10869번 - 사칙연산
 * 링크: https://www.acmicpc.net/problem/10869
 * 분류: 구현, 사칙연산
 * 
 * 문제 설명:
 * - 두 자연수 A와 B가 주어진다.
 * - A+B, A-B, A*B, A/B(몫), A%B(나머지)를 각각 한 줄에 하나씩 출력한다.
 * 
 * 입력:
 * - 첫째 줄에 A와 B가 공백으로 구분되어 주어진다. (1 ≤ A, B ≤ 10,000)
 * 
 * 출력:
 * - 첫째 줄부터 다섯째 줄까지 순서대로 A+B, A-B, A*B, A/B, A%B를 출력한다.
 * 
 * 접근 방법:
 * - 두 수를 입력받아 각각의 사칙연산과 나머지 연산 결과를 차례로 출력한다.
 * - 나눗셈은 정수 나눗셈(몫)으로 출력한다.
 * 
 * 시간복잡도: O(1)
 * 공간복잡도: O(1)
 */

#include <iostream>
using namespace std;

int main() {
    int A, B;
    cin >> A >> B;           // 두 자연수 입력

    cout << A + B << '\n';   // 덧셈
    cout << A - B << '\n';   // 뺄셈
    cout << A * B << '\n';   // 곱셈
    cout << A / B << '\n';   // 몫(정수 나눗셈)
    cout << A % B << '\n';   // 나머지

    return 0;
}
