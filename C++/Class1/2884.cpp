/*
 * 문제: 백준 2884번 - 알람 시계
 * 링크: https://www.acmicpc.net/problem/2884
 * 분류: 구현, 시뮬레이션
 *  
 * 문제 설명:
 * - 현재 시각이 주어졌을 때, 45분 일찍 알람이 울리도록 설정해야 하는 시각을 구하는 문제.
 * 
 * 입력:
 * - 첫째 줄에 현재 시와 분이 공백으로 구분되어 주어진다. (0 ≤ 시 ≤ 23, 0 ≤ 분 ≤ 59)
 * 
 * 출력:
 * - 알람 시각의 시와 분을 공백으로 구분하여 출력한다.
 * 
 * 접근 방법:
 * - 분에서 45를 뺀다. 만약 분이 음수가 되면, 시를 1 감소시키고 분에 60을 더한다.
 * - 시가 음수가 되면 23으로 바꾼다.
 * 
 * 시간복잡도: O(1)
 * 공간복잡도: O(1)
 */

#include <iostream>
using namespace std;

int main() {
    int h, m;
    cin >> h >> m; // 현재 시각 입력 받기

    m -= 45; // 45분 일찍 알람 설정
    if (m < 0) {
        m += 60;
        h -= 1;
        if (h < 0) {
            h = 23;
        }
    }

    cout << h << " " << m;
    return 0;
}
