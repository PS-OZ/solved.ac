/*
 * 문제: 백준 2753번 - 윤년
 * 링크: https://www.acmicpc.net/problem/2753
 * 분류: 구현, 조건문
 * 
 * 문제 설명:
 * - 연도가 주어졌을 때, 윤년이면 1, 아니면 0을 출력하는 문제.
 * - 윤년의 조건:
 *   1. 연도가 4의 배수이면서 100의 배수가 아닐 때
 *   2. 또는 400의 배수일 때
 * 
 * 입력:
 * - 첫째 줄에 연도(1 ≤ 연도 ≤ 4000)가 주어진다.
 * 
 * 출력:
 * - 윤년이면 1, 아니면 0을 출력한다.
 * 
 * 접근 방법:
 * - 조건문을 이용해 윤년의 조건을 판별한다.
 * 
 * 시간복잡도: O(1)
 * 공간복잡도: O(1)
 */

#include <iostream>
using namespace std;

int main() {
    int year;
    cin >> year; // 연도 입력 받기

    // 윤년 판별 조건
    if ((year % 4 == 0 && year % 100 != 0) || (year % 400 == 0)) {
        cout << 1;
    } else {
        cout << 0;
    }

    return 0;
}
