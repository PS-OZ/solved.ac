/*
 * 문제: 백준 2438번 - 별 찍기 - 1
 * 링크: https://www.acmicpc.net/problem/2438
 * 분류: 구현, 반복문
 * 
 * 문제 설명:
 * - 첫째 줄에 N(1 ≤ N ≤ 100)이 주어진다.
 * - 첫째 줄부터 N번째 줄까지 별을 출력한다.
 * - 1번째 줄에는 별 1개, 2번째 줄에는 별 2개, ..., N번째 줄에는 별 N개를 출력한다.
 * 
 * 입력:
 * - 첫째 줄에 N이 주어진다.
 * 
 * 출력:
 * - 첫째 줄부터 N번째 줄까지 차례대로 별을 출력한다.
 * 
 * 접근 방법:
 * - 바깥 for문은 줄 수(N)만큼 반복한다.
 * - 각 줄마다, 해당 줄 번호만큼 별(*)을 출력한 뒤 줄바꿈을 한다.
 * 
 * 시간복잡도: O(N^2)
 * 공간복잡도: O(1)
 */

#include <iostream>
using namespace std;

int main() {
    int n;
    cin >> n; // 출력할 줄 수 입력 받기

    for (int i = 1; i <= n; i++) { // 1번째 줄부터 n번째 줄까지
        for (int j = 1; j <= i; j++) { // i번째 줄에는 별 i개 출력
            cout << "*";
        }
        cout << "\n"; // 줄바꿈
    }

    return 0;
}
