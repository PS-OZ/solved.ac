/*
 * 문제: 백준 10951번 - A+B - 4
 * 링크: https://www.acmicpc.net/problem/10951
 * 분류: 구현, 반복문, 입출력
 * 
 * 문제 설명:
 * - 두 정수 A와 B를 입력받은 다음, A+B를 출력하는 프로그램을 작성한다.
 * - 입력은 여러 개의 테스트 케이스로 이루어져 있으며, 입력의 개수는 주어지지 않는다.
 * - 입력의 끝(EOF, End Of File)이 오면 프로그램을 종료해야 한다.
 * 
 * 입력:
 * - 각 테스트 케이스는 한 줄로 이루어져 있으며, 각 줄에 A와 B가 주어진다. (0 < A, B < 10)
 * 
 * 출력:
 * - 각 테스트 케이스마다 A+B를 한 줄에 하나씩 출력한다.
 * 
 * 접근 방법:
 * - 입력의 개수가 정해지지 않았으므로, 입력 스트림이 끝날 때(EOF)까지 반복해서 입력을 받는다.
 * - C++에서는 `while (cin >> a >> b)` 형태로 EOF를 처리할 수 있다.
 * 
 * 시간복잡도: O(T) (T: 테스트 케이스 개수)
 * 공간복잡도: O(1)
 */

#include <iostream>
using namespace std;

int main() {
    int a, b;
    // 입력이 더 이상 없을 때(EOF)까지 반복
    while (cin >> a >> b) {
        cout << a + b << '\n';
    }
    return 0;
}
