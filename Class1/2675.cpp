/*
 * 문제: 백준 2675번 - 문자열 반복
 * 링크: https://www.acmicpc.net/problem/2675
 * 분류: 문자열, 구현
 * 
 * 문제 설명:
 * - 각 테스트 케이스마다 정수 R과 문자열 S가 주어진다.
 * - 문자열 S의 각 문자를 R번 반복해 새로운 문자열을 만들어 출력한다.
 * 
 * 입력:
 * - 첫째 줄에 테스트 케이스 개수 T가 주어진다.
 * - 각 테스트 케이스마다 반복 횟수 R(1 ≤ R ≤ 8)과 문자열 S(1 ≤ S의 길이 ≤ 20, S는 알파벳 대소문자와 숫자로만 이루어짐)가 공백으로 구분되어 주어진다.
 * 
 * 출력:
 * - 각 테스트 케이스마다 변환된 문자열을 한 줄씩 출력한다.
 * 
 * 접근 방법:
 * - 테스트 케이스 개수만큼 반복한다.
 * - 각 테스트 케이스에서 문자열의 각 문자마다 R번 반복하여 출력한다.
 * 
 * 시간복잡도: O(T * N * R) (T: 테스트 케이스 수, N: 문자열 길이, R: 반복 횟수, 최대 8*20*1000=160,000)
 * 공간복잡도: O(1) (출력 공간 제외)
 */

#include <iostream>
using namespace std;

int main() {
    int t, r;
    string s;
    cin >> t; // 테스트 케이스 개수 입력

    while (t--) {
        cin >> r >> s; // 반복 횟수와 문자열 입력
        for (int i = 0; i < s.length(); i++) {
            for (int k = 0; k < r; k++) {
                cout << s[i]; // 각 문자를 r번 반복 출력
            }
        }
        cout << '\n'; // 테스트 케이스마다 줄바꿈
    }
    return 0;
}
