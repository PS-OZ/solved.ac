/*
 * 문제: 백준 11654번 - 아스키 코드
 * 링크: https://www.acmicpc.net/problem/11654
 * 분류: 구현, 입출력
 * 
 * 문제 설명:
 * - 알파벳 소문자, 대문자, 숫자 0~9 중 하나가 주어졌을 때, 그 문자의 아스키 코드 값을 출력한다.
 * 
 * 입력:
 * - 첫째 줄에 문자 하나가 주어진다.
 * 
 * 출력:
 * - 입력으로 주어진 문자의 아스키 코드 값을 출력한다.
 * 
 * 접근 방법:
 * - 입력받은 문자를 정수형으로 변환하여 출력한다.
 *   (C++에서는 char형을 int형으로 변환하면 해당 문자의 아스키 코드 값이 출력된다.)
 * 
 * 시간복잡도: O(1)
 * 공간복잡도: O(1)
 */

#include <iostream>
using namespace std;

int main() {
    char c;
    cin >> c;           // 문자 입력
    cout << (int)c;     // 아스키 코드 값 출력
    return 0;
}
