/*
 * 문제: 백준 2475번 - 검증수
 * 링크: https://www.acmicpc.net/problem/2475
 * 분류: 구현, 수학
 * 
 * 문제 설명:
 * - 컴퓨터의 고유번호 처음 5자리에 들어가는 5개의 숫자가 주어진다.
 * - 이 5개의 숫자를 각각 제곱한 수의 합을 10으로 나눈 나머지를 '검증수'라고 한다.
 * - 예시: 입력이 0 4 2 5 6 이면, 0^2 + 4^2 + 2^2 + 5^2 + 6^2 = 81, 81 % 10 = 1 이므로 1을 출력한다.
 * 
 * 입력:
 * - 첫째 줄에 0 이상 9 이하의 숫자 5개가 공백으로 구분되어 주어진다.
 * 
 * 출력:
 * - 검증수를 출력한다.
 * 
 * 접근 방법:
 * - 5개의 숫자를 입력받아 각각 제곱한 뒤 모두 더한다.
 * - 그 합을 10으로 나눈 나머지를 출력한다.
 * 
 * 시간복잡도: O(1)
 * 공간복잡도: O(1)
 */

#include <iostream>
using namespace std;

int main() {
    int n, sum = 0;
    // 5개의 숫자를 입력받아 각각 제곱하여 합산
    for (int i = 0; i < 5; i++) {
        cin >> n;
        sum += n * n;
    }
    // 합을 10으로 나눈 나머지(검증수) 출력
    cout << sum % 10;
    return 0;
}
