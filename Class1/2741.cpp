/*
 * 문제: 백준 2741번 - N 찍기
 * 링크: https://www.acmicpc.net/problem/2741
 * 분류: 구현, 반복문
 * 
 * 문제 설명:
 * - 자연수 N(1 ≤ N ≤ 100,000)이 주어진다.
 * - 1부터 N까지 한 줄에 하나씩 출력한다.
 * 
 * 입력:
 * - 첫째 줄에 N이 주어진다.
 * 
 * 출력:
 * - 1부터 N까지 각 수를 한 줄에 하나씩 출력한다.
 * 
 * 접근 방법:
 * - 1부터 N까지 반복문을 사용하여 각 수를 출력한다.
 * 
 * 시간복잡도: O(N)
 * 공간복잡도: O(1)
 */

#include <iostream>
using namespace std;

int main() {
    int n;
    cin >> n; // 출력할 마지막 수 입력 받기

    for (int i = 1; i <= n; i++) {
        cout << i << '\n'; // 각 수를 한 줄에 하나씩 출력
    }

    return 0;
}
