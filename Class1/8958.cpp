/*
 * 문제: 백준 8958번 - OX퀴즈
 * 링크: https://www.acmicpc.net/problem/8958
 * 분류: 구현, 문자열
 * 
 * 문제 설명:
 * - OX퀴즈 결과 문자열이 주어진다. 'O'는 문제를 맞힌 것, 'X'는 틀린 것이다.
 * - 'O'가 연속될수록 가산점이 붙는다. 즉, 연속된 O의 개수만큼 점수를 누적하여 합산한다.
 *   예: "OOXXOXXOOO" → 1+2+0+0+1+0+0+1+2+3 = 10점
 * 
 * 입력:
 * - 첫째 줄에 테스트 케이스의 개수 N이 주어진다.
 * - 각 테스트 케이스는 한 줄에 O와 X로만 이루어진 문자열(길이 1~80)이 주어진다.
 * 
 * 출력:
 * - 각 테스트 케이스마다 점수를 한 줄씩 출력한다.
 * 
 * 접근 방법:
 * - 각 테스트 케이스마다 문자열을 순회하며,
 *   'O'가 나오면 누적 점수를 1 증가시키고 총합에 더한다.
 *   'X'가 나오면 누적 점수를 0으로 초기화한다.
 * 
 * 시간복잡도: O(N * L) (N: 테스트 케이스 개수, L: 문자열 길이)
 * 공간복잡도: O(1) (입력 문자열 저장 공간 제외)
 */

#include <iostream>
#include <string>
using namespace std;

int main() {
    int n;
    cin >> n; // 테스트 케이스 개수 입력
    for (int i = 0; i < n; i++) {
        string s;
        cin >> s; // OX퀴즈 결과 문자열 입력
        int sum = 0;      // 총 점수
        int streak = 0;   // 연속된 O의 개수(가산점)
        for (char c : s) {
            if (c == 'O') {
                streak++;     // 연속 O면 누적 증가
                sum += streak;
            } else {
                streak = 0;   // X면 누적 초기화
            }
        }
        cout << sum << '\n';
    }
    return 0;
}
