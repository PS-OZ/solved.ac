/*
 * 문제: 백준 10171번 - 고양이
 * 링크: https://www.acmicpc.net/problem/10171
 * 분류: 구현, 출력
 * 
 * 문제 설명:
 * - 고양이 그림을 그대로 출력하는 문제.
 * - 역슬래시(\)와 작은따옴표(') 등 특수문자 출력에 주의해야 한다.
 * 
 * 입력:
 * - 없음
 * 
 * 출력:
 * - 아래와 같은 고양이 그림을 출력한다.
 *   \    /\
 *    )  ( ')
 *   (  /  )
 *    \(__)|
 * 
 * 접근 방법:
 * - 이스케이프 문자(\\, \')를 사용하여 그림을 그대로 출력한다.
 * 
 * 시간복잡도: O(1)
 * 공간복잡도: O(1)
 */

#include <iostream>
using namespace std;

int main() {
    cout << "\\    /\\\n";
    cout << " )  ( ')\n";
    cout << "(  /  )\n";
    cout << " \\(__)|";
    return 0;
}
