/*
 * 문제: 백준 2439번 - 별 찍기 - 2
 * 링크: https://www.acmicpc.net/problem/2439
 * 분류: 구현, 반복문
 *  
 * 문제 설명:
 * - 첫째 줄에는 별 1개, 둘째 줄에는 별 2개, ..., N번째 줄에는 별 N개를 출력한다.
 * - 단, 출력되는 별은 오른쪽을 기준으로 정렬되어야 한다.
 * 
 * 입력:
 * - 첫째 줄에 N(1 ≤ N ≤ 100)이 주어진다.
 * 
 * 출력:
 * - 첫째 줄부터 N번째 줄까지 차례대로 별을 오른쪽 정렬로 출력한다.
 * 
 * 접근 방법:
 * - 각 줄마다 먼저 공백을 N-i개 출력한 뒤, 별을 i개 출력한다. (i는 1부터 N까지)
 * - 이중 반복문을 사용하여 공백과 별을 각각 출력한다.
 * 
 * 시간복잡도: O(N^2)
 * 공간복잡도: O(1)
 */

#include <iostream>
using namespace std;

int main() {
    int n;
    cin >> n; // 출력할 줄 수 입력 받기

    for (int i = 1; i <= n; i++) { // 1번째 줄부터 n번째 줄까지
        // 공백 출력: N-i개
        for (int j = 1; j <= n - i; j++) {
            cout << " ";
        }
        // 별 출력: i개
        for (int j = 1; j <= i; j++) {
            cout << "*";
        }
        cout << "\n"; // 줄바꿈
    }

    return 0;
}
